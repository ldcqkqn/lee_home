function navClick(navname){
    if(navname ==='practice'){
        document.location.href = 'https://www.naver.com/';
    }else if(navname ==='make'){
        document.location.href = 'https://www.nate.com/';
    }else if(navname ==='define'){
        document.location.href = 'https://www.hanmail.net/';
    }else{
        document.location.href = 'https://www.google.com/';
    }
}